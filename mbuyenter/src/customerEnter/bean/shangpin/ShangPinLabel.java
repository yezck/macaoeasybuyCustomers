package customerEnter.bean.shangpin;

public class ShangPinLabel {

	private int id;
	private String LabelName;
	private String  content;
	private String  picture;
	private int  price;
	private int  HBgiving;
	private int  JFgiving;
	private int  theorder;
	private String  addtime;
	private String  uptime;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLabelName() {
		return LabelName;
	}
	public void setLabelName(String labelName) {
		LabelName = labelName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getHBgiving() {
		return HBgiving;
	}
	public void setHBgiving(int hBgiving) {
		HBgiving = hBgiving;
	}
	public int getJFgiving() {
		return JFgiving;
	}
	public void setJFgiving(int jFgiving) {
		JFgiving = jFgiving;
	}
	public int getTheorder() {
		return theorder;
	}
	public void setTheorder(int theorder) {
		this.theorder = theorder;
	}
	public String getAddtime() {
		return addtime;
	}
	public void setAddtime(String addtime) {
		this.addtime = addtime;
	}
	public String getUptime() {
		return uptime;
	}
	public void setUptime(String uptime) {
		this.uptime = uptime;
	}
	
	
}
